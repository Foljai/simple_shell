/**
 **_strncpy - copy a string
 *@dest: the destination strings to copied into
 *@src: the source strings
 *@n: the amounts of character to copy
 *Return: the concatenated strings
 */
char *_strncpy(char *dest, char *src, int n)
{
	int i, j;
	char *s = dest;

	i = 0;
	while (src[i] != '\0' && i < n - 1)
	{
		dest[i] = src[i];
		i++;
	}
	if (i < n)
	{
		j = i;
		while (j < n)
		{
			dest[j] = '\0';
			j++;
		}
	}
	return (s);
}

